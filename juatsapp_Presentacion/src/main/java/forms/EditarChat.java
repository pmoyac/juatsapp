    public boolean validarTitulo() {
        if (this.txt_titulo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llene el campo titulo",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        return true;
    }

    public boolean validarContacto() {
        if (this.txt_contacto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llene el campo Contacto",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        if (!this.txt_contacto.getText().matches("^[0-9]{10}$")) {
            JOptionPane.showMessageDialog(null, "Tel√©fono solo acepta numeros de 10 caracteres",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    public boolean agregarParticipante() throws Exception {

        ObjectId con = gu.buscar(this.txt_contacto.getText());
        if (con == null) {
            JOptionPane.showMessageDialog(null, "Contacto no existe",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            List<ObjectId> part = chat.getIntegrantes();
            part.add(con);
            chat.setIntegrantes(part);

            gc.editarChat(chat);

            JOptionPane.showMessageDialog(null, "Integrante Agregado",
                    "Aviso", JOptionPane.INFORMATION_MESSAGE);
            return true;
        }

    }

